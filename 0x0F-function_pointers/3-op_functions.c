#include "3-calc.h"

/**
 * op_add - Will return the addition two ints.
 * @a: int one.
 * @b: The second int.
 *
 * Return: The sum of both.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Will return the difference of a and b.
 * @a: int one.
 * @b: The second int.
 *
 * Return: The difference of both.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Will return the product of a and b.
 * @a: Int one.
 * @b: The second int.
 *
 * Return: The product of both.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Will return the result after division.
 * @a: Int one.
 * @b: THe second int.
 *
 * Return: The result  after the division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Will return the remainder of a and b.
 * @a: Int one.
 * @b: The sccond int.
 *
 * Return: The remainder of both.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
